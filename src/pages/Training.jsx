export default function Training() {
  const [searchTerm, setSearchTerm] = useState('');
  const [openSection, setOpenSection] = useState(null);

  const filtered = searchTerm
    ? sections.filter(s =>
        s.title.includes(searchTerm) ||
        s.content.includes(searchTerm) ||
        s.tags.includes(searchTerm.toLowerCase())
      )
    : sections;

  return (
    <div className="p-6 max-w-4xl mx-auto" dir="rtl">
      <div className="mb-6">
        <h1 className="text-3xl font-bold text-slate-800 flex items-center gap-3">
          <BookOpen className="w-8 h-8 text-blue-600" />
          הדרכת מערכת ארגמן
        </h1>
        <p className="text-gray-500 mt-2">לחצו על נושא כדי לפתוח, או חפשו</p>
      </div>

      <div className="mb-5 flex items-center gap-2">
        <Search className="w-5 h-5 text-gray-400" />
        <Input
          placeholder="חיפוש... (למשל: רכש, גבייה, PDF, עיקבון)"
          value={searchTerm}
          onChange={(e) => { setSearchTerm(e.target.value); setOpenSection(null); }}
          className="bg-white text-base"
        />
      </div>

      <div className="space-y-3">
        {filtered.length === 0 && (
          <p className="text-center text-gray-400 py-8">לא נמצאו תוצאות</p>
        )}
        {filtered.map((section, index) => {
          const realIndex = sections.indexOf(section);
          const isOpen = openSection === realIndex;
          const Icon = section.icon;
          return (
            <Card key={realIndex} className="border-0 shadow-md overflow-hidden">
              <div
                className="flex items-center gap-4 p-4 cursor-pointer hover:bg-slate-50 transition-colors"
                style={{ borderRight: `4px solid ${section.color}` }}
                onClick={() => setOpenSection(isOpen ? null : realIndex)}
              >
                <div className="w-10 h-10 rounded-lg flex items-center justify-center flex-shrink-0" style={{ backgroundColor: `${section.color}15` }}>
                  <Icon size={22} style={{ color: section.color }} />
                </div>
                <h2 className="text-base font-bold text-slate-800 flex-1">{section.title}</h2>
                <span className="text-gray-400 text-xl font-bold">{isOpen ? '−' : '+'}</span>
              </div>
              {isOpen && (
                <CardContent className="pt-0 pb-5 px-5">
                  <div className="bg-slate-50 rounded-lg p-5 text-gray-700 leading-relaxed whitespace-pre-line text-[15px]">
                    {section.content}
                  </div>
                </CardContent>
              )}
            </Card>
          );
        })}
      </div>
    </div>
  );
}
